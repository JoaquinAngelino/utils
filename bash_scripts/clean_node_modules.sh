#!/bin/bash

# Remove node_modules directories up to a specified depth
# Defaults to depth=1 (searches immediate subdirectories only)

DEPTH=1
INCLUDE_ROOT=false

usage() {
    cat <<EOF
Usage: $0 [-d depth]

Options:
    -d N   Search up to N directory levels for node_modules (default 1).
                     N must be an integer >= 1. For example:
                         -d 1  -> searches ./<dir>/node_modules (immediate subdirs)
                         -d 2  -> searches ./<dir>/.../<dir>/node_modules (two levels)
    -h     Show this help
    -r     Include the top-level ./node_modules in removal
EOF
    exit 1
}

while getopts "d:h" opt; do
    case "$opt" in
        d)
            DEPTH="$OPTARG"
            ;;
        r)
            INCLUDE_ROOT=true
            ;;
        h)
            usage
            ;;
        *)
            usage
            ;;
    esac
done

if ! [[ "$DEPTH" =~ ^[0-9]+$ ]] || [ "$DEPTH" -lt 1 ]; then
    echo "Error: depth must be an integer >= 1" >&2
    usage
fi

# find's node_modules depth relative to '.' is one level deeper than directories
# Default behavior: skip top-level ./node_modules (MINDEPTH=2).
# If INCLUDE_ROOT is true, set MINDEPTH=1 to include ./node_modules.
if [ "$INCLUDE_ROOT" = true ]; then
    MINDEPTH=1
else
    MINDEPTH=2
fi
MAXDEPTH=$((DEPTH + 1))

echo "Searching for node_modules directories with mindepth=$MINDEPTH maxdepth=$MAXDEPTH (include_root=$INCLUDE_ROOT)"

matches=$(find . -mindepth "$MINDEPTH" -maxdepth "$MAXDEPTH" -type d -name node_modules -prune)

if [ -z "$matches" ]; then
    echo "No node_modules directories found within specified depth."
    exit 0
fi

echo "Found the following node_modules directories:" 
echo "$matches"

while IFS= read -r nm; do
    # skip empty lines
    [ -z "$nm" ] && continue
    echo "Removing: $nm"
    rm -rf -- "$nm"
done <<< "$matches"

echo "Done!"
